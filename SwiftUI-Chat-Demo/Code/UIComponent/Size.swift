import Foundation
import SwiftUI

/// Enumeration for defining commonly used padding values in the app.
/// Each case represents a padding value with a specific `CGFloat` value.
public enum Padding: CGFloat {
    case p25 = 25  // Padding value of 25 points
    case p50 = 50  // Padding value of 50 points
    case p35 = 35  // Padding value of 35 points
    case lg = 64   // Large padding value of 64 points
    case xl = 88   // Extra large padding value of 88 points
}

